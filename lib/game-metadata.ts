// lib/game-metadata.ts
import type { Metadata } from 'next';
import { getGameById } from './games';

/**
 * Generate Next.js metadata for a game page
 */
export async function generateGameMetadata(gameId: string): Promise<Metadata> {
  const game = await getGameById(gameId);

  if (!game) {
    return {
      title: 'Game Not Found',
      description: 'The requested game could not be found.',
    };
  }

  const title = `${game.title} - DevOps Daily`;
  const description = game.description;
  const ogImage = `/images/games/${gameId}-og.png`;

  return {
    title: game.title,
    description,
    alternates: {
      canonical: game.href,
    },
    openGraph: {
      title,
      description,
      type: 'website',
      url: game.href,
      images: [
        {
          url: ogImage,
          width: 1200,
          height: 630,
          alt: game.title,
        },
      ],
    },
    twitter: {
      card: 'summary_large_image',
      title,
      description,
      images: [ogImage],
    },
  };
}
